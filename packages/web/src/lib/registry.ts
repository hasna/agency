export interface HasnaPackage {
  /** Short name, e.g. "todos" */
  name: string;
  /** npm package name, e.g. "@hasna/todos" */
  npm: string;
  /** One-line description */
  description: string;
  /** Binary entry points shipped by the package */
  bins: { cli?: string; mcp?: string; serve?: string };
  /** Whether the package uses a database (SQLite via @hasna/cloud or better-sqlite3) */
  hasDb: boolean;
  /** Whether the package exposes an MCP server */
  hasMcp: boolean;
  /** Whether the package exposes an HTTP server */
  hasHttp: boolean;
  /** Data directory name under ~/.hasna/ */
  dataDir: string;
}

