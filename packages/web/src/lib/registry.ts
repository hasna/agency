/** Look up a package by short name */
export function findPackage(name: string): HasnaPackage | undefined {
  return REGISTRY.find((p) => p.name === name);
}

/** Get all packages that have an MCP server */
export function mcpPackages(): HasnaPackage[] {
  return REGISTRY.filter((p) => p.hasMcp);
}

/** Get all packages that have a database */
export function dbPackages(): HasnaPackage[] {
  return REGISTRY.filter((p) => p.hasDb);
}

/** Get all packages that have an HTTP server */
export function httpPackages(): HasnaPackage[] {
  return REGISTRY.filter((p) => p.hasHttp);
}

/** Total package count */
export const PACKAGE_COUNT = REGISTRY.length;
